    private void shope_Qty()
    {
        String ProName = TextProNam.getText();
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con1 = DriverManager.getConnection("jdbc:mysql://localhost/storemanagemetsystem","root","");
            Insert = con1.prepareStatement("select * from product where Product=?");
            Insert.setString(1, ProName);
            RS = Insert.executeQuery();
            while(RS.next())
            {
                int CurrentQty;
                CurrentQty = RS.getInt("Quantity");
                int price = Integer.parseInt(txtPrice.getText());
                int NewQty = Integer.parseInt(textQTY.getText());  
                int ToTal = price * NewQty;
                
                if(NewQty > CurrentQty)
                {
                    JOptionPane.showMessageDialog(this, "Current Product quantity is: "+CurrentQty);
                    JOptionPane.showMessageDialog(this, "Quantity is not Enough");                    
                }
                else
                {
                    model = (DefaultTableModel)jTablesh.getModel();
                    model.addRow(new Object[]
                    {
                        textProCod.getText(),
                        TextProNam.getText(),
                        txtPrice.getText(),
                        textQTY.getText(),
                        ToTal,
                    });
                    textProCod.setText("");
                    TextProNam.setText("");
                    txtPrice.setText("");
                    textQTY.setText("");
                    textProCod.requestFocus();
                }
                
                int Sum = 0;
                for(int i=0; i<jTablesh.getRowCount(); i++)
                {
                    Sum = Sum + Integer.parseInt(jTablesh.getValueAt(i, 4).toString());
                }
                jTextTotBill.setText(Integer.toString(Sum));
                
            }
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ShopeSalesman.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(ShopeSalesman.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    

    private void Sales()
    {
        DateTimeFormatter daTe = DateTimeFormatter.ofPattern("yyyy/MM/dd");
        LocalDateTime tiMe = LocalDateTime.now();
        String DATE = daTe.format(tiMe);
        String totalBill = jTextTotBill.getText();
        String Pay = jTextPayAmt.getText();
        String ReAmt = jTextReAmt.getText();      

        int lastInserId = 0;
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con1 = DriverManager.getConnection("jdbc:mysql://localhost/storemanagemetsystem","root","");
            String query = "INSERT INTO sales(Date,TotalBil,Pay,ReturnA) VALUES (?,?,?,?)";
            Insert = con1.prepareStatement(query,Statement.RETURN_GENERATED_KEYS);
            Insert.setString(1, DATE);           
            Insert.setString(2, totalBill);
            Insert.setString(3, Pay);
            Insert.setString(4, ReAmt);            
            Insert.executeUpdate();

            ResultSet genKeyResult = Insert.getGeneratedKeys();
            if(genKeyResult.next())
            {
                lastInserId = genKeyResult.getInt(1);
            }
            
            int Row = jTablesh.getRowCount();
            String Query1 = "INSERT INTO sales_product(Sales_Id,Product_Id,Sell_Price,Quantity,Total) VALUES (?,?,?,?,?)";
            Insert = con1.prepareStatement(Query1,Statement.RETURN_GENERATED_KEYS);
            
            String Product_Id = "";
            String Price = "";
            String Quantity = "";
            int total = 0;
            
            for(int i=0; i<jTablesh.getRowCount(); i++)
            {
                Product_Id = (String)jTablesh.getValueAt(i, 0);
                Price = (String)jTablesh.getValueAt(i, 2);
                Quantity = (String)jTablesh.getValueAt(i, 3);
                total = (int)jTablesh.getValueAt(i, 4);
                
                Insert.setInt(1, lastInserId);
                Insert.setString(2, Product_Id);
                Insert.setString(3, Price);
                Insert.setString(4, Quantity);
                Insert.setInt(5, total);
                Insert.executeUpdate(); 
            }
            
            String Query2 = "UPDATE product SET Quantity=Quantity-? WHERE BarCode=?";
            Insert = con1.prepareStatement(Query2);
            
            for(int i=0; i<jTablesh.getRowCount(); i++)
            {
                Product_Id = (String)jTablesh.getValueAt(i, 0);
                Quantity = (String)jTablesh.getValueAt(i, 3);
                
                Insert.setString(1, Quantity);
                Insert.setString(2, Product_Id);
                Insert.executeUpdate(); 
            }
            
            
            Insert.addBatch();
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddModel.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AddModel.class.getName()).log(Level.SEVERE, null, ex);
        }   
    }
    
